def make_upper(source_string: str) -> str:
    """
     The make_upper function takes a string as an argument,
     converts the character case to uppercase,
     and returns the result as a string.
    """
    return source_string.upper()


def make_title(source_string: str) -> str:
    """
     The function takes a string as an argument,
     capitalizes the first letters of each word,
     and returns the result as a string.
    """
    return source_string.title()
